package com.bth.model;

import java.io.Serializable;

public class VendorDAO implements Serializable{
    private  int id;
    private String name,email,password,mobile,vendorId,type,status="Block";

    public VendorDAO() {
    }

    public VendorDAO(String name, String email, String password, String mobile, String vendorId, String type) {
     this.id = id;    
        this.name = name;
        this.email = email;
        this.password = password;
        this.mobile = mobile;
        this.vendorId = vendorId;
        this.type = type;
    }
 public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getVendorId() {
        return vendorId;
    }

    public void setVendorId(String vendorId) {
        this.vendorId = vendorId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
